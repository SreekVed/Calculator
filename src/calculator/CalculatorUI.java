    static double temp=0;
    static int operation=0;
    private void ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActionPerformed
        
        Object s = evt.getSource();
        JButton b = (JButton) s;
         if(s==plus) {
            temp = Double.valueOf(calc.getText());
            calc.setText("");
            operation=1;
        }
         else if(s==minus) {
            temp = Double.parseDouble(calc.getText());
            calc.setText("");
            operation=2;
        }
         else if(s==mult) {
            temp = Double.parseDouble(calc.getText());
            calc.setText("");
            operation=3;
        }
         else if(s==div) {
            temp = Double.parseDouble(calc.getText());
            calc.setText("");
            operation=4;
        }
         
         else if(s==eq) {
            double init = Double.parseDouble(calc.getText());
            double result=0;
            switch(operation) {
            
                case 0 : break;
                case 1 : result = temp+init;
                break;
                case 2 : result = temp-init;
                    break;
                case 3 : result = temp*init;
                break;
                case 4 : result = temp/init;
                break;
            
            }
            calc.setText(String.valueOf(result));
        }
         
         else {
        this.calc.setText(calc.getText()+b.getText());
        if(evt.getSource()==decimal) decimal.setEnabled(false);
        this.Clear.setEnabled(true);
        this.Delete.setEnabled(true);
        plus.setEnabled(true);
        minus.setEnabled(true);
        div.setEnabled(true);
        mult.setEnabled(true);
        eq.setEnabled(true);
        
         }
        
    }//GEN-LAST:event_ActionPerformed

    
    
